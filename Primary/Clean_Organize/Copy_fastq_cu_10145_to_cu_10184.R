  from.batch = paste0(from.dir, '/Batch', batch.num)
  to.batch = paste0(to.dir, '/Primary_', batch.num)

  if (dir.exists(to.batch))  unlink(to.batch, recursive=TRUE)

  system(paste0('rsync -r --times --copy-links ', from.batch, '/ ', to.batch))

  }

